<h2 id="inicio">Serviço</h2>

<p id="tela">Os Serviços de Intendência e de Saúde (médicos, dentistas e farmacêuticos) trabalham na paz e na guerra para a manutenção do homem, pelo atendimento às suas necessidades de sustento e sanitárias.</p>

<p id="tela">Os oficiais de Intendência são mestres no suprimento e nas finanças.</p>

<p id="tela">Os músicos são formados em outras escolas militares, de acordo com a sua área de atuação; assim, além da já citada EsSEx, temos as: Escola de Sargentos de Logística – EsSLog, Escola de Comunicações – EsCom, Escola de Instrução Especializada – EsIE e o Centro de Instrução de Aviação do Exército – CIAVEx.</p>